# int, float, str, bool
print(type(True))
print(type(False))

# 비교 -> bool : >,>=,<,<=,==,!=
print(5>3)
print(type(5>3))
print(5<3)
print(type(5<3))
print(5==3)
print(5!=3)

#논리
print(not True)
print(not 5>3)

#파이썬 에서 논리연산을 사용하면 0은 거짓, 0이 아니면 참
print(not 5)
print(not 0)
print(0 == True)

#파이썬에서 논리연사을 사용하지면 '' 은 거짓
print(not '')

